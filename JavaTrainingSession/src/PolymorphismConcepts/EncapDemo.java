package PolymorphismConcepts;

public class EncapDemo {
	
	private int nino;
	private String nameOfTheNino;
	private double salary;
	
	public int getNino() {
		return nino;
	}
	public void setNino(int nino) {
		this.nino = nino;
	}
	public String getNameOfTheNino() {
		return nameOfTheNino;
	}
	public void setNameOfTheNino(String nameOfTheNino) {
		this.nameOfTheNino = nameOfTheNino;
	}
	public double getSalary() {
		return salary;
	}
	public void setSalary(double salary) {
		this.salary = salary;
	}
	
	
	

}
